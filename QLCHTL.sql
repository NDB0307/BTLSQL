CREATE TABLE CON_NGUOI
(
ID_CN NVARCHAR(50) NOT NULL PRIMARY KEY,
TEN NVARCHAR(50),
NGAY_SINH DATE,
GIOI_TINH NVARCHAR(50),
SDT NVARCHAR(50),
)
GO
CREATE TABLE CHUC_VU
(
MA_CV NVARCHAR(50) NOT NULL PRIMARY KEY,
TEN_CV NVARCHAR(50),
LUONG_CO_BAN INT
)
GO
CREATE TABLE NHAN_VIEN
(
ID_NV NVARCHAR(50) NOT NULL PRIMARY KEY REFERENCES 
CON_NGUOI(ID_CN),
TRANG_THAI NVARCHAR(50),
MA_CV NVARCHAR(50) REFERENCES CHUC_VU(MA_CV),
ID_QLY NVARCHAR(50) REFERENCES NHAN_VIEN(ID_NV)
)
GO
CREATE TABLE LUONG
(
ID_NV NVARCHAR(50) NOT NULL PRIMARY KEY REFERENCES 
NHAN_VIEN(ID_NV),
THUONG INT,
SO_CA_LAM INT,
MA_CV NVARCHAR(50) REFERENCES CHUC_VU(MA_CV),
)
GO
CREATE TABLE CA_LAM
(
MA_CA NVARCHAR(50) NOT NULL PRIMARY KEY,
THOI_GIAN TIME,
NGAY DATE
)
GO
CREATE TABLE CHAM_CONG
(
MA_CA NVARCHAR(50) REFERENCES CA_LAM(MA_CA),
ID_NV NVARCHAR(50) REFERENCES NHAN_VIEN(ID_NV)
)
GO
CREATE TABLE NHA_CUNG_CAP
(
MA_NCC NVARCHAR(50) NOT NULL PRIMARY KEY,
TEN NVARCHAR(50),
SDT NVARCHAR(50),
DIA_CHI NVARCHAR(50)
)
GO
CREATE TABLE KHACH_HANG
(
ID_KH NVARCHAR(50) NOT NULL PRIMARY KEY REFERENCES 
CON_NGUOI(ID_CN),
KHTT NVARCHAR(50),
DIEM INT,
)
GO
CREATE TABLE HOA_DON
(
MA_HD NVARCHAR(50) NOT NULL PRIMARY KEY,
THOI_GIAN DATE,
TONG_TIEN INT,
ID_NV NVARCHAR(50) REFERENCES NHAN_VIEN(ID_NV),
ID_KH NVARCHAR(50) REFERENCES KHACH_HANG(ID_KH),
MA_CA NVARCHAR(50) REFERENCES CA_LAM(MA_CA)
)
GO
CREATE TABLE HANG_HOA(
MA_HH NVARCHAR(50) NOT NULL PRIMARY KEY,
TEN_HH NVARCHAR(50),
SO_LUONG INT,
GIA INT,
LOAI_HH NVARCHAR(50),
NGAY_NHAP DATE,
HAN_SU_DUNG DATE,
MA_NCC NVARCHAR(50)
)
GO
CREATE TABLE HANG_NHAP
(
MA_NHAP_HANG NVARCHAR(50) NOT NULL PRIMARY KEY,
SO_LUONG INT,
NGAY_NHAP DATE,
DON_GIA NVARCHAR(50),
MA_NCC NVARCHAR(50) REFERENCES 
NHA_CUNG_CAP(MA_NCC),
MA_HH NVARCHAR(50) REFERENCES HANG_HOA(MA_HH)
)
GO
CREATE TABLE CTHD
(
MA_CTHD NVARCHAR(50) NOT NULL PRIMARY KEY,
SO_LUONG INT,
GIA INT,
MA_HD NVARCHAR(50) REFERENCES HOA_DON(MA_HD),
MA_HH NVARCHAR(50) REFERENCES HANG_HOA(MA_HH)
)
GO
CREATE TABLE BO_HANG
(
MA_BO NVARCHAR(50) NOT NULL PRIMARY KEY,
TEN NVARCHAR(50),
SO_LUONG INT,
TONG_GIA INT,
NGAY_BO DATE,
MA_HH NVARCHAR(50) REFERENCES HANG_HOA(MA_HH)
)
GO
