-- Thống kê danh sách nhân viên và chức vụ tương ứng
SELECT 
    CN.TEN, 
    CV.TEN_CV, 
    CV.LUONG_CO_BAN, 
    NV.TRANG_THAI
FROM NHAN_VIEN NV
JOIN CON_NGUOI CN ON NV.ID_NV = CN.ID_CN
JOIN CHUC_VU CV ON NV.MA_CV = CV.MA_CV;
-- Tìm các mặt hàng sắp hết hạn sử dụng
SELECT 
    MA_HH, 
    TEN_HH, 
    HAN_SU_DUNG, 
    SO_LUONG
FROM HANG_HOA
WHERE HAN_SU_DUNG <= '31/12/2025';
-- Tính tổng doanh thu theo từng nhân viên
SELECT 
    NV.ID_NV, 
    CN.TEN, 
    SUM(HD.TONG_TIEN) AS TONG_DOANH_THU
FROM HOA_DON HD
JOIN NHAN_VIEN NV ON HD.ID_NV = NV.ID_NV
JOIN CON_NGUOI CN ON NV.ID_NV = CN.ID_CN
GROUP BY NV.ID_NV, CN.TEN;
-- Liệt kê top 5 khách hàng có số điểm tích lũy cao nhất
SELECT TOP 5 
    CN.TEN, 
    KH.KHTT, 
    KH.DIEM
FROM KHACH_HANG KH
JOIN CON_NGUOI CN ON KH.ID_KH = CN.ID_CN
ORDER BY KH.DIEM DESC;
-- Thống kê số lượng hàng hóa nhập về từ mỗi nhà cung cấp
SELECT 
    NCC.TEN AS TEN_NHA_CUNG_CAP, 
    COUNT(HH.MA_HH) AS SO_LOAI_HANG_CUNG_CAP,
    SUM(HH.SO_LUONG) AS TONG_SO_LUONG_TON
FROM NHA_CUNG_CAP NCC
LEFT JOIN HANG_HOA HH ON NCC.MA_NCC = HH.MA_NCC
GROUP BY NCC.TEN;